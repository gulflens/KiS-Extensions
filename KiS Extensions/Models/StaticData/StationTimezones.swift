// MARK: - Station Timezones

/// Maps IATA station codes to IANA timezone identifiers for correct
/// cross-timezone flight time calculations.
enum StationTimezones {

    static func timeZone(for iataCode: String) -> TimeZone? {
        guard let id = zones[iataCode.uppercased()] else { return nil }
        return TimeZone(identifier: id)
    }

    static var allCodes: [String] {
        zones.keys.sorted()
    }

    static func cityName(for iataCode: String) -> String? {
        guard let id = zones[iataCode.uppercased()] else { return nil }
        let city = id.components(separatedBy: "/").last ?? id
        return city.replacingOccurrences(of: "_", with: " ")
    }

